[Lili Cheng (Microsoft), Tom Ngo (NextPage), Gary Benitt (Goowy), Chris Thomas (Intel), Kevin Lynch (Adobe)]<br>
<br>
<i>Windows and Mac OS, the dominant computer interfaces of our time, are two decades old.  And the standard office productivity software suite is not much younger.  Have they reached the end of their usefulness?  And if so, what will replace them?  It's no secret that web-based applications are encroaching on the desktop's turf, but how far will that go, and will the webtop actually deliver anything the desktop can't?  This roundtable discussion will bring together experts to consider the challenges for the desktop in an era of pervasive broadband connectivity.</i>

